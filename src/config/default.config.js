/** ----------------------------------------
     Create Constructor
 ---------------------------------------- */

const setDefaults = config => {

    /** ----------------------------------------
        Configuration
     ---------------------------------------- */

    const defaults = [

        /** ----------------------------------------
             Config
         ---------------------------------------- */

        {
            name: 'config',
            value: config
        },

        /** ----------------------------------------
            Data
         ---------------------------------------- */

        {
            name: 'data',
            value: {}
        },

        /** ----------------------------------------
            URL
         ---------------------------------------- */

        {
            name: 'url',
            value: window.location.href
        }

    ];

    return defaults;
};

/** ----------------------------------------
    Exports
 ---------------------------------------- */

export default setDefaults;